# to show a multi way decision based on several condition, we use if elif statement

math = 60

#above 90 => Grade A
#ABOVE 75 => Grade B
#ELSE => Grade C

if math > 90:
    print("grade: A")

elif math > 75:    # => ELIF ek se jyda bhi use kr skte hai
    print("grade B")

else:
    print("grade C")


